# Program that takes two numbers as input and prints their sum, product, and quotient, storing each result in a separate variable.

num1 = int(input("Enter the first number: "))
num2 = int(input("Enter the second number: "))

sum_of_num = num1 + num2
product_of_num = num1 * num2
quotient_of_num = num1 / num2  

print("The sum of numbers:", sum_of_num)
print("The product of numbers:", product_of_num)
print("The quotation of numbers:", quotient_of_num)
