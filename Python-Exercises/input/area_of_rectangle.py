# program that calculates the area of a rectangle based on user input for its length and width.

# prompt for user to defining the length and width of rectangle..
width = float(input("Enter the width of rectangle: "))
length = float(input("Enter the length of rectangle: "))

# Calculate the area of rectangle using formula A = length * width
area = length * width

# print area of rectangle
print("The area of rectangle is:", area)